    private void btnSamaDenganActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSamaDenganActionPerformed
       switch(Operasi){
           //tambah
           case 1:
               bil2 = Double.parseDouble(angka.replace(",", "."));
               jumlah = bil1 + bil2;
               angka = Double.toString(jumlah);
               hasil.setText(angka);
               break;
               
           //kurang
           case  2:
               bil2 = Double.parseDouble(angka.replace(",", "."));
               jumlah = bil1 - bil2;
               angka = Double.toString(jumlah);
               hasil.setText(angka);
               break;
              
           //kali
           case 3:
                bil2 = Double.parseDouble(angka.replace(",", "."));
                jumlah = bil1 * bil2;
                angka = Double.toString(jumlah);
                hasil.setText(angka);
                break;
               
           //bagi
           case 4:
               bil2 = Double.parseDouble(angka.replace(",", "."));
               jumlah = bil1 / bil2;
               angka = Double.toString(jumlah);
               hasil.setText(angka);
               break;
               
           //modulus
           case 5:
              bil2 = Double.parseDouble(angka.replace(",", "."));
              jumlah = bil1 % bil2;
              angka = Double.toString(jumlah);
              hasil.setText(angka);
              break;
               
           default:
               break;      
       }
    }//GEN-LAST:event_btnSamaDenganActionPerformed


    private void btnBackspaceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackspaceActionPerformed
         // pengkondisian untuk memeriksa apakah ada angka yang telah diinput di kalkulator
         if (angka.length() > 0) {
             //Jika ada, karakter terakhir dari angka akan dihapus
             angka = angka.substring(0, angka.length() - 1);
             hasil.setText(angka);//menampilkan angka terbaru
    }
    }//GEN-LAST:event_btnBackspaceActionPerformed


    // Method untuk menampilkan data mahasiswa dalam tabel
    public final void viewDataTabelMhs() {
       String[] namakolom = {"NIM", "Nama", "Alamat", "Mata Kuliah", "Nilai Akhir"};
    
       //menyesuaikan ukuran array objekmahasiswa dengan jumlah kolom baru
       Object[][] objekmahasiswa = new Object[dataNilaiMahasiswa.getALL().size()][5];
       int i = 0;
       
       // Perulangan untuk mengisi array objekmahasiswa dengan data dari dataNilaiMahasiswa
       for (Mahasiswa mhs : dataNilaiMahasiswa.getALL()) {
        Object arrayMahasiswa[] = {
            mhs.getNIM(),
            mhs.getNama(),
            mhs.getAlamat(),
            mhs.getMataKuliah(),  
            mhs.getNilaiAkhir()   
        };

        objekmahasiswa[i] = arrayMahasiswa;
        i++;
    }
       
       modelMahasiswa = new DefaultTableModel(objekmahasiswa, namakolom);
       nilaiMhsTB.setModel(modelMahasiswa);
    
    }
    

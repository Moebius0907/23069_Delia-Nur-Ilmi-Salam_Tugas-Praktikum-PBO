    //Method untuk memanggil nilaiAkhir setiap input nilai 
    private void callNilaiAkhir() {
        //eksepsi untuk input nilai agar valid
        try {
            if (!Nilai1.getText().equals("") && !Nilai2.getText().equals("") && !Nilai3.getText().equals("")
                && !Nilai4.getText().equals("") && !Nilai5.getText().equals("")) {
                double nilai1 = Double.parseDouble(Nilai1.getText());
                double nilai2 = Double.parseDouble(Nilai2.getText());
                double nilai3 = Double.parseDouble(Nilai3.getText());
                double nilai4 = Double.parseDouble(Nilai4.getText());
                double nilai5 = Double.parseDouble(Nilai5.getText());
                double nilaiAkhir = (nilai1 * 0.10) + (nilai2 * 0.15) + (nilai3 * 0.25) + (nilai4 * 0.15) + (nilai5 * 0.35);
                NilaiAkhir.setText(String.valueOf(nilaiAkhir));
            }
        }catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Pastikan semua nilai diisi dengan angka yang valid.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

